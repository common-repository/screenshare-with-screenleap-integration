/**
 * Get Timezone Title.
 *
 * @param string $timezone
 * @return string
 */
function get_timezone_title( $timezone ) {
	$timezones = get_timezones();
	if ( ! empty( $timezones[ $timezone ] ) ) {
		return $timezones[ $timezone ];
	} else {
		return '';
	}
}
